; CastlePalm — Pong (v3)
; Left paddle: player (Up/Down). Right paddle: ball-tracking AI.
; Tall paddles, thin dashed centre line, 16x16 numeric score, first-to-5 wins.

; --- MMIO ---
INPUT      EQU $100000
VRAM_ADDR  EQU $101000
VRAM_DATA  EQU $101004
PAL_INDEX  EQU $101008
PAL_DATA   EQU $10100A
OAM_INDEX  EQU $10100C
OAM_DATA   EQU $10100E
SQ0_PERIOD EQU $102000
SQ0_VOL    EQU $102002
SQ0_CTRL   EQU $102003

; --- input bits ---
UP     EQU 1
DOWN   EQU 2
START  EQU 256

; --- constants ---
LEFTX    EQU 16
RIGHTX   EQU 296          ; 320 - 16 - 8
PADH     EQU 32
PADSPD   EQU 3
PADMAX   EQU 192          ; 224 - PADH
BALLMAXY EQU 216          ; 224 - 8
WINSCORE EQU 5
DIGIT0   EQU 16           ; 16x16 digits: tile DIGIT0 + digit*4
LET16    EQU 64           ; 16x16 letters A-Z: tile LET16 + letter*4

; --- RAM state ---
ballX  EQU $000100
ballY  EQU $000102
ballVX EQU $000104
ballVY EQU $000106
padLY  EQU $000108
padRY  EQU $00010A
scoreL EQU $00010C
scoreR EQU $00010E
gover  EQU $000110
sfxT   EQU $000112
started EQU $000114        ; 0 = title screen, 1 = playing
diff    EQU $000116        ; 0=easy 1=normal 2=hard (AI paddle speed = diff+1)

  ORG $300000
  DA start
  DA 0
  DA 0
  DA 0

start:
  ; palette entry 1 = white
  MOV R0, #1
  STB R0, [PAL_INDEX]
  MOV R0, #$7FFF
  STW R0, [PAL_DATA]
  ; tile 1 = solid colour 1 (VRAM $20)
  MOV R0, #$20
  STB R0, [VRAM_ADDR]
  MOV R0, #0
  STB R0, [VRAM_ADDR+1]
  STB R0, [VRAM_ADDR+2]
  MOV R1, #32
  MOV R2, #$11
filltile:
  STB R2, [VRAM_DATA]
  SUB R1, #1
  BNE filltile
  ; tile 2 = thin 2px vertical line (VRAM $40)
  MOV R0, #$40
  STB R0, [VRAM_ADDR]
  MOV R0, #0
  STB R0, [VRAM_ADDR+1]
  STB R0, [VRAM_ADDR+2]
  MOV R3, #8
t2row:
  MOV R0, #0
  STB R0, [VRAM_DATA]
  MOV R0, #$01
  STB R0, [VRAM_DATA]
  MOV R0, #$10
  STB R0, [VRAM_DATA]
  MOV R0, #0
  STB R0, [VRAM_DATA]
  SUB R3, #1
  BNE t2row
  ; copy digit font (1280 bytes) to VRAM tile 16 ($200)
  MOV R0, #0
  STB R0, [VRAM_ADDR]
  MOV R0, #2
  STB R0, [VRAM_ADDR+1]
  MOV R0, #0
  STB R0, [VRAM_ADDR+2]
  LDA A0, #digitdata
  MOV R1, #1280
cpdigit:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE cpdigit
  ; copy letter font (3328 bytes) to VRAM tile 64 ($800)
  MOV R0, #0
  STB R0, [VRAM_ADDR]
  MOV R0, #8
  STB R0, [VRAM_ADDR+1]
  MOV R0, #0
  STB R0, [VRAM_ADDR+2]
  LDA A0, #letterdata
  MOV R1, #3328
cpletter:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE cpletter
  CALL resetgame

mainloop:
  ; advance the sound-effect timer; silence SQ0 when it expires
  LDW R0, [sfxT]
  CMP R0, #0
  BEQ sfxidle
  SUB R0, #1
  STW R0, [sfxT]
  CMP R0, #0
  BNE sfxidle
  MOV R0, #0
  STB R0, [SQ0_CTRL]
sfxidle:
  ; --- title screen until Start is pressed ---
  LDW R0, [started]
  CMP R0, #0
  BNE sf_run
  LDW R0, [INPUT]
  MOV R1, R0
  AND R1, #16          ; A = easy
  BEQ sf_nA
  MOV R1, #0
  BRA sf_go
sf_nA:
  MOV R1, R0
  AND R1, #32          ; B = normal
  BEQ sf_nB
  MOV R1, #1
  BRA sf_go
sf_nB:
  MOV R1, R0
  AND R1, #64          ; X = hard
  BEQ sf_nX
  MOV R1, #2
  BRA sf_go
sf_nX:
  AND R0, #START       ; Start = normal (default)
  BEQ sf_title
  MOV R1, #1
sf_go:
  STW R1, [diff]
  MOV R0, #1
  STW R0, [started]
  CALL resetgame
  BRA playing
sf_title:
  CALL drawtitle
  WAIT
  BRA mainloop
sf_run:
  LDW R0, [gover]
  CMP R0, #0
  BEQ playing
  LDW R0, [INPUT]
  AND R0, #START
  BEQ overdraw
  CALL resetgame
overdraw:
  CALL drawsprites
  WAIT
  BRA mainloop

playing:
  ; ---- left paddle from input ----
  LDW R0, [INPUT]
  MOV R1, R0
  AND R1, #UP
  BEQ noup
  LDW R2, [padLY]
  SUB R2, #PADSPD
  STW R2, [padLY]
noup:
  MOV R1, R0
  AND R1, #DOWN
  BEQ nodown
  LDW R2, [padLY]
  ADD R2, #PADSPD
  STW R2, [padLY]
nodown:
  LDW R2, [padLY]
  CMP R2, #0
  BGE lclo
  MOV R2, #0
lclo:
  CMP R2, #PADMAX
  BLE lchi
  MOV R2, #PADMAX
lchi:
  STW R2, [padLY]

  ; ---- right paddle AI (speed = diff+1; tracks ball centre) ----
  LDW R4, [diff]
  ADD R4, #1           ; aispd = 1 (easy) / 2 (normal) / 3 (hard)
  LDW R2, [padRY]
  LDW R3, [ballY]
  SUB R3, #12          ; aim paddle centre at ball centre
  CMP R2, R3
  BGE aiup
  ADD R2, R4
  BRA aiclamp
aiup:
  SUB R2, R4
aiclamp:
  CMP R2, #0
  BGE rclo
  MOV R2, #0
rclo:
  CMP R2, #PADMAX
  BLE rchi
  MOV R2, #PADMAX
rchi:
  STW R2, [padRY]

  ; ---- move ball ----
  LDW R0, [ballX]
  LDW R1, [ballVX]
  ADD R0, R1
  STW R0, [ballX]
  LDW R0, [ballY]
  LDW R1, [ballVY]
  ADD R0, R1
  STW R0, [ballY]

  ; ---- bounce top ----
  LDW R0, [ballY]
  CMP R0, #0
  BGE btop
  MOV R0, #0
  STW R0, [ballY]
  LDW R1, [ballVY]
  NEG R1
  STW R1, [ballVY]
  MOV R0, #30
  MOV R1, #4
  CALL sfx
btop:
  ; ---- bounce bottom ----
  LDW R0, [ballY]
  CMP R0, #BALLMAXY
  BLE bbot
  MOV R0, #BALLMAXY
  STW R0, [ballY]
  LDW R1, [ballVY]
  NEG R1
  STW R1, [ballVY]
  MOV R0, #30
  MOV R1, #4
  CALL sfx
bbot:

  ; ---- left paddle collision ----
  LDW R0, [ballX]
  CMP R0, #24
  BGE lpno
  MOV R3, R0
  ADD R3, #8
  CMP R3, #LEFTX
  BLE lpno
  LDW R1, [ballY]
  LDW R2, [padLY]
  MOV R3, R2
  ADD R3, #PADH
  CMP R1, R3
  BGE lpno
  MOV R3, R1
  ADD R3, #8
  CMP R3, R2
  BLE lpno
  LDW R1, [ballVX]
  CMP R1, #0
  BGE lpno
  NEG R1
  STW R1, [ballVX]
  MOV R0, #30
  MOV R1, #4
  CALL sfx
  ; english: hit near the paddle top/bottom -> steep ball you can aim
  LDW R1, [ballY]
  LDW R2, [padLY]
  SUB R1, R2           ; rel = ball top - paddle top (0..32 within the paddle)
  CMP R1, #10
  BGE lp_nt
  MOV R1, #3           ; top third -> steep up
  NEG R1
  STW R1, [ballVY]
  BRA lpno
lp_nt:
  CMP R1, #22
  BLE lpno             ; middle -> keep current angle
  MOV R1, #3           ; bottom third -> steep down
  STW R1, [ballVY]
lpno:

  ; ---- right paddle collision ----
  LDW R0, [ballX]
  MOV R3, R0
  ADD R3, #8
  CMP R3, #RIGHTX
  BLE rpno
  CMP R0, #304
  BGE rpno
  LDW R1, [ballY]
  LDW R2, [padRY]
  MOV R3, R2
  ADD R3, #PADH
  CMP R1, R3
  BGE rpno
  MOV R3, R1
  ADD R3, #8
  CMP R3, R2
  BLE rpno
  LDW R1, [ballVX]
  CMP R1, #0
  BLE rpno
  NEG R1
  STW R1, [ballVX]
  MOV R0, #30
  MOV R1, #4
  CALL sfx
rpno:

  ; ---- scoring ----
  LDW R0, [ballX]
  CMP R0, #0
  BGE noscl
  MOV R0, #120
  MOV R1, #24
  CALL sfx
  LDW R1, [scoreR]
  ADD R1, #1
  STW R1, [scoreR]
  CALL resetball
  CMP R1, #WINSCORE
  BLT noscl
  MOV R2, #2
  STW R2, [gover]
noscl:
  LDW R0, [ballX]
  CMP R0, #312
  BLE noscr
  MOV R0, #120
  MOV R1, #24
  CALL sfx
  LDW R1, [scoreL]
  ADD R1, #1
  STW R1, [scoreL]
  CALL resetball
  CMP R1, #WINSCORE
  BLT noscr
  MOV R2, #1
  STW R2, [gover]
noscr:

  CALL drawsprites
  WAIT
  BRA mainloop

resetball:
  MOV R0, #152
  STW R0, [ballX]
  MOV R0, #108
  STW R0, [ballY]
  RET

resetgame:
  CALL clearoam        ; wipe title sprites so "PUSH START" can't linger as "TART"
  MOV R0, #0
  STW R0, [scoreL]
  STW R0, [scoreR]
  STW R0, [gover]
  STW R0, [sfxT]
  STB R0, [SQ0_CTRL]
  MOV R0, #96
  STW R0, [padLY]
  STW R0, [padRY]
  MOV R0, #2
  STW R0, [ballVX]
  STW R0, [ballVY]
  CALL resetball
  RET

; sfx: start a square blip on SQ0. R0 = period, R1 = timer (frames).
sfx:
  STW R0, [SQ0_PERIOD]
  MOV R0, #12
  STB R0, [SQ0_VOL]
  MOV R0, #1
  STB R0, [SQ0_CTRL]
  STW R1, [sfxT]
  RET

; emit one sprite: X=R4, Y=R5, tile=R6, attr-lo (size)=R3, enabled. Uses R0 only.
emit:
  STB R4, [OAM_DATA]
  MOV R0, R4
  SHR R0, #8
  STB R0, [OAM_DATA]
  STB R5, [OAM_DATA]
  MOV R0, R5
  SHR R0, #8
  STB R0, [OAM_DATA]
  STB R6, [OAM_DATA]
  MOV R0, R6
  SHR R0, #8
  STB R0, [OAM_DATA]
  STB R3, [OAM_DATA]
  MOV R0, #$80
  STB R0, [OAM_DATA]
  RET

; clear all 128 sprites (zero the 1 KiB OAM) so stale sprites don't linger
clearoam:
  MOV R0, #0
  STW R0, [OAM_INDEX]
  MOV R1, #1024
clo_lp:
  MOV R0, #0
  STB R0, [OAM_DATA]
  SUB R1, #1
  BNE clo_lp
  RET

drawsprites:
  MOV R0, #0
  STW R0, [OAM_INDEX]
  MOV R3, #0               ; size 8
  ; ball
  LDW R4, [ballX]
  LDW R5, [ballY]
  MOV R6, #1
  CALL emit
  ; left paddle (4 stacked)
  LDW R5, [padLY]
  MOV R7, #4
lpd:
  MOV R4, #LEFTX
  MOV R6, #1
  CALL emit
  ADD R5, #8
  SUB R7, #1
  BNE lpd
  ; right paddle (4 stacked)
  LDW R5, [padRY]
  MOV R7, #4
rpd:
  MOV R4, #RIGHTX
  MOV R6, #1
  CALL emit
  ADD R5, #8
  SUB R7, #1
  BNE rpd
  ; centre line (7 thin dashes, tile 2)
  MOV R5, #8
  MOV R7, #7
dsh:
  MOV R4, #156
  MOV R6, #2
  CALL emit
  ADD R5, #32
  SUB R7, #1
  BNE dsh
  ; scores (16x16: tile = DIGIT0 + score*4)
  MOV R3, #$10             ; size 16
  MOV R4, #132
  MOV R5, #6
  LDW R6, [scoreL]
  SHL R6, #2
  ADD R6, #DIGIT0
  CALL emit
  MOV R4, #172
  MOV R5, #6
  LDW R6, [scoreR]
  SHL R6, #2
  ADD R6, #DIGIT0
  CALL emit
  RET

; ---- title screen: a PONG wordmark + PUSH START, with a paddle/ball graphic ----
drawtitle:
  MOV R0, #0
  STW R0, [OAM_INDEX]
  MOV R3, #0               ; size 8
  ; ball (centre)
  MOV R4, #156
  MOV R5, #108
  MOV R6, #1
  CALL emit
  ; left paddle (4 stacked, mid height)
  MOV R5, #96
  MOV R7, #4
ti_lp:
  MOV R4, #LEFTX
  MOV R6, #1
  CALL emit
  ADD R5, #8
  SUB R7, #1
  BNE ti_lp
  ; right paddle
  MOV R5, #96
  MOV R7, #4
ti_rp:
  MOV R4, #RIGHTX
  MOV R6, #1
  CALL emit
  ADD R5, #8
  SUB R7, #1
  BNE ti_rp
  ; "PONG" (16x16 letters)
  MOV R3, #$10
  MOV R4, #128
  MOV R5, #48
  LDA A1, #str_pong
  CALL prtext
  ; difficulty menu — tap A / B / X (or Start = normal)
  MOV R3, #$10
  MOV R4, #112
  MOV R5, #148
  LDA A1, #str_easy
  CALL prtext
  MOV R4, #96
  MOV R5, #172
  LDA A1, #str_norm
  CALL prtext
  MOV R4, #112
  MOV R5, #196
  LDA A1, #str_hard
  CALL prtext
  RET

; ---- prtext(R4=x, R5=y, R3=size, A1=letter-index string): emit 16x16 letters ----
; bytes: 0-25 = A-Z, 26 = space, $FF = end
prtext:
pt_loop:
  LDB R6, [A1]
  CMP R6, #$FF
  BEQ pt_done
  CMP R6, #26
  BEQ pt_space
  SHL R6, #2
  ADD R6, #LET16
  CALL emit
pt_space:
  ADD R4, #16
  INC A1
  BRA pt_loop
pt_done:
  RET

str_pong:
  DB 15,14,13,6,$FF                   ; PONG
str_easy:
  DB 0,26,4,0,18,24,$FF               ; A EASY
str_norm:
  DB 1,26,13,14,17,12,0,11,$FF        ; B NORMAL
str_hard:
  DB 23,26,7,0,17,3,$FF               ; X HARD

