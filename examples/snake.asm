; CastlePalm — Snake (Phase 4 game)
; Grid playfield on BG0 (so the body has no sprite limit). Ring buffer for the
; body, an occupancy grid for O(1) self-collision, a deterministic RNG for food,
; and a 2-digit sprite score. D-pad steers; Start restarts after a game over.

; --- MMIO ---
INPUT     EQU $100000
VRAM_ADDR EQU $101000
VRAM_DATA EQU $101004
PAL_INDEX EQU $101008
PAL_DATA  EQU $10100A
OAM_INDEX EQU $10100C
OAM_DATA  EQU $10100E
PPU_CTRL  EQU $101018

; --- input bits ---
UP    EQU 1
DOWN  EQU 2
LEFT  EQU 4
RIGHT EQU 8
START EQU 256

; --- constants ---
GW    EQU 40            ; grid width (320/8)
GH    EQU 28            ; grid height (224/8)
PLAYTOP EQU 4           ; first playable row (rows 0-2 score HUD, row 3 line)
SPEED EQU 6             ; advance every 6 frames
T_EMPTY EQU 0
T_SNAKE EQU 1
T_FOOD  EQU 2
T_WALL  EQU 3           ; HUD separator line
DIGIT0  EQU 16          ; digit tiles 16..25
LET     EQU 32          ; letter tiles 32..57 (A..Z)
BIGDIG  EQU 64          ; 16x16 score digits, 4 tiles each (64..103)
LET16   EQU 104         ; 16x16 letters A..Z, 4 tiles each (104..207)
RMASK   EQU $7FF        ; ring index mask (2048 entries)
SQ0_PERIOD EQU $102000  ; APU square 0
SQ0_VOL    EQU $102002
SQ0_CTRL   EQU $102003

; --- RAM ---
dir     EQU $000100     ; 0 up, 1 down, 2 left, 3 right
nextDir EQU $000102
headX   EQU $000104
headY   EQU $000106
foodX   EQU $000108
foodY   EQU $00010A
score   EQU $00010C
gover   EQU $00010E
mtimer  EQU $000110
rng     EQU $000112
slen    EQU $000114
hptr    EQU $000116
tptr    EQU $000118
state   EQU $00011A     ; 0 title, 1 play, 2 over
sfxT    EQU $00011C     ; sound-effect countdown
RING    EQU $001000     ; 2048 x 2-byte packed (y<<8|x)
OCC     EQU $002000     ; 1 byte per cell (y*40+x)

  ORG $300000
  DA start
  DA 0
  DA 0
  DA 0

start:
  ; palette 0=black, 1=green (snake), 2=red (food)
  MOV R0, #0
  STB R0, [PAL_INDEX]
  MOV R0, #$0000
  STW R0, [PAL_DATA]
  MOV R0, #$03E0
  STW R0, [PAL_DATA]
  MOV R0, #$001F
  STW R0, [PAL_DATA]
  MOV R0, #$4210
  STW R0, [PAL_DATA]      ; palette 3 = gray (HUD line)
  ; tile1 = solid colour 1 (green) at VRAM $20
  MOV R0, #$20
  STB R0, [VRAM_ADDR]
  MOV R0, #0
  STB R0, [VRAM_ADDR+1]
  STB R0, [VRAM_ADDR+2]
  MOV R1, #32
  MOV R2, #$11
st_t1:
  STB R2, [VRAM_DATA]
  SUB R1, #1
  BNE st_t1
  ; tile2 = solid colour 2 (red) at VRAM $40
  MOV R0, #$40
  STB R0, [VRAM_ADDR]
  MOV R0, #0
  STB R0, [VRAM_ADDR+1]
  STB R0, [VRAM_ADDR+2]
  MOV R1, #32
  MOV R2, #$22
st_t2:
  STB R2, [VRAM_DATA]
  SUB R1, #1
  BNE st_t2
  ; tile3 = thin HUD line (2px) at VRAM $60
  MOV R0, #$60
  STB R0, [VRAM_ADDR]
  MOV R0, #0
  STB R0, [VRAM_ADDR+1]
  STB R0, [VRAM_ADDR+2]
  LDA A0, #linetile
  MOV R1, #32
st_t3:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE st_t3
  ; digit font -> tile 16 ($200), 320 bytes
  MOV R0, #0
  STB R0, [VRAM_ADDR]
  MOV R0, #2
  STB R0, [VRAM_ADDR+1]
  MOV R0, #0
  STB R0, [VRAM_ADDR+2]
  LDA A0, #digitdata
  MOV R1, #320
st_cpd:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE st_cpd
  ; letter font -> tile 32 ($400), 832 bytes
  MOV R0, #0
  STB R0, [VRAM_ADDR]
  MOV R0, #4
  STB R0, [VRAM_ADDR+1]
  MOV R0, #0
  STB R0, [VRAM_ADDR+2]
  LDA A0, #letterdata
  MOV R1, #832
st_cpl:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE st_cpl
  ; 16x16 score font -> tile 64 ($800), 1280 bytes
  MOV R0, #0
  STB R0, [VRAM_ADDR]
  MOV R0, #8
  STB R0, [VRAM_ADDR+1]
  MOV R0, #0
  STB R0, [VRAM_ADDR+2]
  LDA A0, #bigdigit
  MOV R1, #1280
st_cpb:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE st_cpb
  ; 16x16 letter font -> tile 104 ($D00), 3328 bytes
  MOV R0, #0
  STB R0, [VRAM_ADDR]
  MOV R0, #$D
  STB R0, [VRAM_ADDR+1]
  MOV R0, #0
  STB R0, [VRAM_ADDR+2]
  LDA A0, #letter16
  MOV R1, #3328
st_cpw:
  LDB R2, [A0]
  STB R2, [VRAM_DATA]
  INC A0
  SUB R1, #1
  BNE st_cpw
  ; enable BG0
  MOV R0, #1
  STW R0, [PPU_CTRL]
  ; seed RNG and show the title screen
  MOV R0, #$ACE1
  STW R0, [rng]
  MOV R0, #0
  STW R0, [state]
  STW R0, [sfxT]
  CALL drawtitle

mainloop:
  LDW R0, [state]
  CMP R0, #1
  BEQ playing
  ; title / game over: wait for Start to (re)start
  LDW R0, [INPUT]
  AND R0, #START
  BEQ ml_wait
  CALL resetgame
ml_wait:
  CALL sfxtick
  WAIT
  BRA mainloop

playing:
  CALL readinput
  LDW R0, [mtimer]
  ADD R0, #1
  STW R0, [mtimer]
  CMP R0, #SPEED
  BLT pl_draw
  MOV R0, #0
  STW R0, [mtimer]
  CALL tick
  LDW R0, [gover]
  CMP R0, #0
  BEQ pl_draw
  MOV R0, #2
  STW R0, [state]
  CALL drawgameover
pl_draw:
  CALL drawscore
  BRA ml_wait

; ---- read the D-pad into nextDir (no 180-degree reversal) ----
readinput:
  LDW R0, [INPUT]
  LDW R1, [dir]
  MOV R2, R0
  AND R2, #UP
  BEQ ri_nu
  CMP R1, #1
  BEQ ri_nu
  MOV R2, #0
  STW R2, [nextDir]
ri_nu:
  MOV R2, R0
  AND R2, #DOWN
  BEQ ri_nd
  CMP R1, #0
  BEQ ri_nd
  MOV R2, #1
  STW R2, [nextDir]
ri_nd:
  MOV R2, R0
  AND R2, #LEFT
  BEQ ri_nl
  CMP R1, #3
  BEQ ri_nl
  MOV R2, #2
  STW R2, [nextDir]
ri_nl:
  MOV R2, R0
  AND R2, #RIGHT
  BEQ ri_nr
  CMP R1, #2
  BEQ ri_nr
  MOV R2, #3
  STW R2, [nextDir]
ri_nr:
  RET

; ---- advance the snake one cell ----
tick:
  LDW R0, [nextDir]
  STW R0, [dir]
  LDW R4, [headX]
  LDW R5, [headY]
  LDW R2, [dir]
  CMP R2, #0
  BNE tk_1
  SUB R5, #1
  BRA tk_chk
tk_1:
  CMP R2, #1
  BNE tk_2
  ADD R5, #1
  BRA tk_chk
tk_2:
  CMP R2, #2
  BNE tk_3
  SUB R4, #1
  BRA tk_chk
tk_3:
  ADD R4, #1
tk_chk:
  CMP R4, #0
  BGE tk_b1
tk_die:
  CALL die
  RET
tk_b1:
  CMP R4, #GW
  BLT tk_b2
  BRA tk_die
tk_b2:
  CMP R5, #PLAYTOP
  BGE tk_b3
  BRA tk_die
tk_b3:
  CMP R5, #GH
  BLT tk_food
  BRA tk_die
tk_food:
  LDW R3, [foodX]
  CMP R4, R3
  BNE tk_nofood
  LDW R3, [foodY]
  CMP R5, R3
  BNE tk_nofood
  ; ate: grow (no tail removal), score++, new food
  MOV R0, R4
  MOV R1, R5
  CALL addhead
  LDW R3, [score]
  ADD R3, #1
  STW R3, [score]
  MOV R0, #80
  MOV R1, #4
  CALL sfx
  CALL placefood
  RET
tk_nofood:
  CALL removetail
  MOV R0, R4
  MOV R1, R5
  CALL getocc
  CMP R2, #0
  BEQ tk_addok
  CALL die
  RET
tk_addok:
  MOV R0, R4
  MOV R1, R5
  CALL addhead
  RET

; ---- addhead(R0=x, R1=y): push head onto ring, mark occ, draw, set head ----
addhead:
  STW R0, [headX]
  STW R1, [headY]
  ; ring[hptr] = (y<<8)|x
  MOV R2, R1
  SHL R2, #8
  OR R2, R0
  LDW R3, [hptr]
  SHL R3, #1
  LDA A0, #RING
  ADD A0, R3
  STW R2, [A0]
  LDW R3, [hptr]
  ADD R3, #1
  AND R3, #RMASK
  STW R3, [hptr]
  LDW R3, [slen]
  ADD R3, #1
  STW R3, [slen]
  ; occ(x,y) = 1
  MOV R2, R1
  SHL R2, #5
  MOV R3, R1
  SHL R3, #3
  ADD R2, R3
  ADD R2, R0
  LDA A0, #OCC
  ADD A0, R2
  MOV R3, #1
  STB R3, [A0]
  ; draw snake tile
  MOV R2, #T_SNAKE
  CALL setcell
  RET

; ---- removetail(): pop the tail off the ring, clear occ + draw empty ----
removetail:
  LDW R3, [tptr]
  SHL R3, #1
  LDA A0, #RING
  ADD A0, R3
  LDW R2, [A0]
  MOV R0, R2
  AND R0, #$FF          ; x
  MOV R1, R2
  SHR R1, #8            ; y
  ; occ(x,y) = 0
  MOV R2, R1
  SHL R2, #5
  MOV R3, R1
  SHL R3, #3
  ADD R2, R3
  ADD R2, R0
  LDA A0, #OCC
  ADD A0, R2
  MOV R3, #0
  STB R3, [A0]
  MOV R2, #T_EMPTY
  CALL setcell
  LDW R3, [tptr]
  ADD R3, #1
  AND R3, #RMASK
  STW R3, [tptr]
  LDW R3, [slen]
  SUB R3, #1
  STW R3, [slen]
  RET

; ---- getocc(R0=x, R1=y) -> R2 = occupancy byte ----
getocc:
  MOV R2, R1
  SHL R2, #5
  MOV R3, R1
  SHL R3, #3
  ADD R2, R3
  ADD R2, R0
  LDA A0, #OCC
  ADD A0, R2
  LDB R2, [A0]
  RET

; ---- setcell(R0=x, R1=y, R2=tile): write a BG0 map entry ----
setcell:
  MOV R3, R1
  SHL R3, #6
  ADD R3, R0
  SHL R3, #2            ; (y*64 + x) * 4 = map byte offset
  STB R3, [VRAM_ADDR]
  MOV R0, R3
  SHR R0, #8
  STB R0, [VRAM_ADDR+1]
  MOV R0, #1
  STB R0, [VRAM_ADDR+2] ; bit16 -> BG0 map base $10000
  STB R2, [VRAM_DATA]   ; tile (bits 0-7)
  MOV R0, #0
  STB R0, [VRAM_DATA]
  STB R0, [VRAM_DATA]
  STB R0, [VRAM_DATA]
  RET

; ---- next RNG value (xorshift16) -> R0 ----
nextrng:
  LDW R0, [rng]
  MOV R1, R0
  SHL R1, #7
  XOR R0, R1
  MOV R1, R0
  SHR R1, #9
  XOR R0, R1
  MOV R1, R0
  SHL R1, #8
  XOR R0, R1
  STW R0, [rng]
  RET

; ---- place food on a random empty cell ----
placefood:
pf_retry:
  CALL nextrng
  MOV R1, R0
  AND R1, #63
  CMP R1, #GW
  BGE pf_retry
  MOV R2, R0
  SHR R2, #8
  AND R2, #31
  CMP R2, #PLAYTOP     ; keep food below the HUD line
  BLT pf_retry
  CMP R2, #GH
  BGE pf_retry
  MOV R0, R1            ; x
  MOV R1, R2           ; y
  CALL getocc
  CMP R2, #0
  BNE pf_retry
  STW R0, [foodX]
  STW R1, [foodY]
  MOV R2, #T_FOOD
  CALL setcell
  RET

; ---- draw the 2-digit score as sprites ----
drawscore:
  MOV R0, #0
  STW R0, [OAM_INDEX]
  LDW R5, [score]
  MOV R4, #0
ds_div:
  CMP R5, #10
  BLT ds_done
  SUB R5, #10
  ADD R4, #1
  BRA ds_div
ds_done:
  MOV R6, R4
  SHL R6, #2
  ADD R6, #BIGDIG          ; tens tile = BIGDIG + digit*4
  MOV R0, #6
  MOV R1, #4
  CALL emitdigit
  MOV R6, R5
  SHL R6, #2
  ADD R6, #BIGDIG          ; ones tile
  MOV R0, #24
  MOV R1, #4
  CALL emitdigit
  RET

; ---- emitdigit(R0=x, R1=y, R6=tile): one 16x16 sprite ----
emitdigit:
  STB R0, [OAM_DATA]
  MOV R2, R0
  SHR R2, #8
  STB R2, [OAM_DATA]
  STB R1, [OAM_DATA]
  MOV R2, R1
  SHR R2, #8
  STB R2, [OAM_DATA]
  STB R6, [OAM_DATA]
  MOV R2, R6
  SHR R2, #8
  STB R2, [OAM_DATA]
  MOV R2, #$10            ; attr lo: sprite size 16x16
  STB R2, [OAM_DATA]
  MOV R2, #$80            ; attr hi: enable
  STB R2, [OAM_DATA]
  RET

; ---- clear the playfield (map + occupancy) ----
clearboard:
  MOV R5, #0
cb_y:
  MOV R4, #0
cb_x:
  MOV R0, R4
  MOV R1, R5
  MOV R2, #0
  CALL setcell
  MOV R2, R5
  SHL R2, #5
  MOV R3, R5
  SHL R3, #3
  ADD R2, R3
  ADD R2, R4
  LDA A0, #OCC
  ADD A0, R2
  MOV R3, #0
  STB R3, [A0]
  ADD R4, #1
  CMP R4, #GW
  BLT cb_x
  ADD R5, #1
  CMP R5, #GH
  BLT cb_y
  RET

; ---- (re)start a game ----
resetgame:
  CALL clearboard
  CALL drawhud
  MOV R0, #0
  STW R0, [score]
  STW R0, [gover]
  STW R0, [mtimer]
  STW R0, [hptr]
  STW R0, [tptr]
  STW R0, [slen]
  MOV R0, #1
  STW R0, [state]
  MOV R0, #3
  STW R0, [dir]
  STW R0, [nextDir]
  MOV R0, #18
  MOV R1, #14
  CALL addhead
  MOV R0, #19
  MOV R1, #14
  CALL addhead
  MOV R0, #20
  MOV R1, #14
  CALL addhead
  CALL placefood
  MOV R0, #150           ; start chirp (immediate audio feedback)
  MOV R1, #6
  CALL sfx
  RET

; ---- draw the HUD separator line at row 3 ----
drawhud:
  MOV R4, #0
dh_loop:
  MOV R0, R4
  MOV R1, #3
  MOV R2, #T_WALL
  CALL setcell
  ADD R4, #1
  CMP R4, #GW
  BLT dh_loop
  RET

; ---- print16(R4=x, R5=y, A1=letter-index string): 16x16 text, 2 cells/char ----
; bytes: 0-25 = A-Z, 26 = space, $FF = end
print16:
p16_loop:
  LDB R2, [A1]
  CMP R2, #$FF
  BEQ p16_done
  CMP R2, #26
  BEQ p16_adv
  SHL R2, #2
  ADD R2, #LET16        ; base tile for this letter
  MOV R6, R2
  MOV R0, R4            ; top-left
  MOV R1, R5
  CALL setcell
  MOV R0, R4            ; top-right
  ADD R0, #1
  MOV R1, R5
  MOV R2, R6
  ADD R2, #1
  CALL setcell
  MOV R0, R4            ; bottom-left
  MOV R1, R5
  ADD R1, #1
  MOV R2, R6
  ADD R2, #2
  CALL setcell
  MOV R0, R4            ; bottom-right
  ADD R0, #1
  MOV R1, R5
  ADD R1, #1
  MOV R2, R6
  ADD R2, #3
  CALL setcell
p16_adv:
  ADD R4, #2
  INC A1
  BRA p16_loop
p16_done:
  RET

; ---- title screen ----
drawtitle:
  CALL clearboard
  MOV R4, #15
  MOV R5, #6
  LDA A1, #str_snake
  CALL print16
  MOV R4, #10
  MOV R5, #11
  LDA A1, #str_push
  CALL print16
  MOV R4, #9
  MOV R5, #15
  LDA A1, #str_move
  CALL print16
  ; little snake + food graphic (green body tiles + a red pellet)
  LDA A1, #snakeart
  MOV R2, #T_SNAKE
sa_loop:
  LDB R0, [A1]
  CMP R0, #$FF
  BEQ sa_done
  INC A1
  LDB R1, [A1]
  INC A1
  MOV R2, #T_SNAKE
  CALL setcell
  BRA sa_loop
sa_done:
  MOV R0, #22
  MOV R1, #21
  MOV R2, #T_FOOD
  CALL setcell
  RET

; little snake body coords (tile x,y pairs; $FF ends) drawn near the bottom
snakeart:
  DB 16,22, 17,22, 18,22, 18,21, 19,21, 20,21, $FF

; ---- game-over overlay (drawn over the dead board) ----
drawgameover:
  MOV R4, #11
  MOV R5, #10
  LDA A1, #str_over
  CALL print16
  MOV R4, #10
  MOV R5, #14
  LDA A1, #str_push
  CALL print16
  RET

; ---- sfx(R0=period, R1=timer frames): blip on SQ0 ----
sfx:
  STW R0, [SQ0_PERIOD]
  MOV R0, #12
  STB R0, [SQ0_VOL]
  MOV R0, #1
  STB R0, [SQ0_CTRL]
  STW R1, [sfxT]
  RET

; ---- advance the sfx timer; silence SQ0 when it expires ----
sfxtick:
  LDW R0, [sfxT]
  CMP R0, #0
  BEQ sfx_idle
  SUB R0, #1
  STW R0, [sfxT]
  CMP R0, #0
  BNE sfx_idle
  MOV R0, #0
  STB R0, [SQ0_CTRL]
sfx_idle:
  RET

; ---- die: end the game with a low beep ----
die:
  MOV R3, #1
  STW R3, [gover]
  MOV R0, #400
  MOV R1, #20
  CALL sfx
  RET

; --- letter-index strings (0-25 = A-Z, 26 = space, $FF = end) ---
str_snake:
  DB 18,13,0,10,4,$FF                       ; SNAKE
str_push:
  DB 15,20,18,7,26,18,19,0,17,19,$FF        ; PUSH START
str_over:
  DB 6,0,12,4,26,14,21,4,17,$FF             ; GAME OVER
str_move:
  DB 0,17,17,14,22,18,26,12,14,21,4,$FF     ; ARROWS MOVE

; --- thin 2px line tile (rows 3-4 lit, colour 3) ---
linetile:
  DB $00,$00,$00,$00,$00,$00,$00,$00,$00,$00,$00,$00
  DB $33,$33,$33,$33,$33,$33,$33,$33
  DB $00,$00,$00,$00,$00,$00,$00,$00,$00,$00,$00,$00

